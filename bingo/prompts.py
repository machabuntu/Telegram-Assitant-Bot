"""AI prompt templates for /bingo."""


def build_bingo_prompt(topic: str) -> str:
    return (
        f'Сгенерируй мемное бинго 5×5 на тему "{topic}".\n\n'
        "Требования:\n"
        "- РОВНО 25 пунктов — по одному на каждую ячейку сетки 5×5.\n"
        "- Каждый пункт помечен координатами row и col (целые числа от 1 до 5).\n"
        "  row=1 — верхний ряд, row=5 — нижний; col=1 — левый столбец, col=5 — правый.\n"
        "- Каждая позиция (row, col) встречается РОВНО один раз. Центр тоже генерируется — без FREE.\n"
        "- Весь текст пунктов на русском языке.\n"
        "- Стиль: мемное бинго с умеренным юмором, отсылки и шутки внутри заданной темы.\n"
        "- Сложность: пункты должны быть нишевыми и специфичными, чтобы среднестатистическому "
        "человеку было трудно собрать линию за один вечер. Избегай очевидных заголовков темы "
        "и слишком общих формулировок.\n"
        "- Длина каждого пункта: не более 45 символов, максимум 2–3 короткие фразы.\n"
        "- Без нумерации, markdown, кавычек вокруг всего пункта и переносов \\n внутри text.\n\n"
        "Верни ТОЛЬКО валидный JSON без markdown-обёрток:\n"
        "{\n"
        f'  "topic": "{topic}",\n'
        '  "cells": [\n'
        '    {"row": 1, "col": 1, "text": "..."},\n'
        "    ...\n"
        "  ]\n"
        "}"
    )
